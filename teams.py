def team_years_in_division(url, div):
    """
    Find the years during which the team in question was a member of the the top division
    This would include:
        pre-1937, all teams
        University Division
        Division I
        Division I-A
    Some teams have a complicated history
    """
    
    soup = get_url(url)
    team_urls = [link.get('href')[:-9] for link in soup.find_all('a')
        if 'active' in link.get('href') or
        'discontinued' in link.get('href')]
    team_names = [link.text for link in soup.find_all('a')
        if 'active' in link.get('href') or
        'discontinued' in link.get('href')]
    start_dates = []
    for t in team_urls:
        found = 0
        soup = get_url('http://cfbdatawarehouse.com/data/' + t + 'index.php')
        for b in soup.find_all('b'):
            if div == b.text[11:]:
                if found > 0:
                    print('{:}'.format(t))
                    break
                found += 1
                try:
                    if 'XX' in b.text[5:9]:
                        start_dates.append((int(b.text[:4]), 2016))
                    else:
                        start_dates.append((int(b.text[:4]), int(b.text[5:9])))
                except ValueError:
                    print('{:}'.format(t))
        if not found:
            start_dates.append('')
    for t, y in zip(team_names, start_dates):
        try:
            print('{:}\tlist(range({:}, {:}+1))'.format(t, y[0], y[1]))
        except:
            print('{:}'.format(t))